public class Package {

    private final ArrayList<Gift> gifts;

    public Package() {
        this.gifts = new ArrayList<>();
    }

    public void addGift(Gift gift) {
        this.gifts.add(gift);
    }

    public int totalWeight() {
        return gifts.stream()
                .map(Gift::getWeight)
                .reduce(0, Integer::sum);
    }
}
